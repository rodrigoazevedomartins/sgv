    private void btnBuscarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarVendaActionPerformed
        // TODO add your handling code here:
        venda = new Venda();
        cliente = new Cliente();
        usuario = new Usuario();
        pagamento = new Pagamento();
        venda.setCliente(cliente);
        venda.setUsuario(usuario);
        venda.setPagamento(pagamento);
        try {   
            if (cbxfiltro.getSelectedIndex() == 0){
                venda.setCodvenda(Integer.parseInt(txtFiltro.getText()));
                System.out.print(venda);
            }

            if (cbxfiltro.getSelectedIndex() == 1){
                cliente = new Cliente();
                cliente.setNome(txtFiltro.getText());
                venda.setCliente(cliente);
            }

            if (cbxfiltro.getSelectedIndex() == 2){
                usuario = new Usuario();
                usuario.setNome(txtFiltro.getText());
                venda.setUsuario(usuario);
            }

            if (cbxfiltro.getSelectedIndex() == 3){
                pagamento = new Pagamento();
                pagamento.setNomeTipo(txtFiltro.getText());
                venda.setPagamento(pagamento);
            }

        } catch (ErroValidacaoException ex) {
            Logger.getLogger(frmVendas.class.getName()).log(Level.SEVERE, null, ex);
        }

        vendas = vendadao.buscarVendas(venda);

        preenchetabela(vendas);
    }//GEN-LAST:event_btnBuscarVendaActionPerformed

