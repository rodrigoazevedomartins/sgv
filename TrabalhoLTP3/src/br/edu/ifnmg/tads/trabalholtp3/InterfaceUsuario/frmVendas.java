    private void preenchetabela(List<Venda> vendas){
        DefaultTableModel tabelavenda = new DefaultTableModel();
        tabelavenda.addColumn("Código Venda");
        tabelavenda.addColumn("Nome Cliente");
        tabelavenda.addColumn("Nome Usuario");
        tabelavenda.addColumn("Forma de Pagamento");
        tabelavenda.addColumn("Valor da Venda");
        for (Venda venda : vendas){
            double valor = 0;
            Vector v =  new Vector();
            itens = vendaprodutodao.ListarItens(venda.getCodvenda());
            
            for (VendaProduto vendaproduto : itens){
                valor = valor + (vendaproduto.getQuantidade() * vendaproduto.getProduto().getValorunitariovenda());
            }
            
            v.add(venda);
            v.add(venda.getCliente());
            v.add(venda.getUsuario());
            v.add(venda.getPagamento());
            v.add(valor);
            tabelavenda.addRow(v);
        }
        
        tblVendas.setModel(tabelavenda);
    }
    
    
